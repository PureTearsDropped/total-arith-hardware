#!/usr/bin/env python3
"""状態符号を **トリット** で。符号トリットの 0 = 「符号不明」。

  符号トリット s ∈ {−1, 0, +1} = 負 / **不明** / 正
  境界トリット b ∈ {−1, 0, +1} = **|x| ≤ v** / **|x| = v（確定）** / **|x| ≥ v**

問い: ① 符号の代数は gate9（既存のトリット積）か
      ② 境界の代数はどのゲートか
      ③ 「境界なし」は 4 番目の状態が要るか
"""
from sedenion_tensor_logic import gate9, gate27

NAME_S = {1: "**正**", 0: "**不明**", -1: "**負**"}
NAME_B = {-1: "**≤v**", 0: "**=v**", 1: "**≥v**"}

print("=" * 76)
print("① **符号の代数は gate9 か** — 符号トリットの積")
print("=" * 76)
print("  欲しい規則（§7.6.4.1）: 積の符号は符号の積。**不明は吸収的**")
print(f"\n  {'a':>8}{'b':>10}{'gate9(a,b)':>14}{'欲しい答え':>14}{'一致':>8}")
ok = n = 0
for a in (1, 0, -1):
    for b in (1, 0, -1):
        g = gate9(a, b)
        want = 0 if (a == 0 or b == 0) else a*b     # **不明(0)は吸収的**
        ok += (g == want); n += 1
        print(f"  {NAME_S[a]:>8}{NAME_S[b]:>10}{NAME_S[g]:>14}{NAME_S[want]:>14}"
              f"{('✓' if g == want else '**✗**'):>8}")
print(f"\n  一致: **{ok}/{n}**")
print("""
  ⟹ **gate9 が符号の代数そのものである。** 新しいゲートは要らない。
     （トリット積は 0 を吸収的に扱う ＝ **「不明 × 何か = 不明」がタダで手に入る**）""")

print()
print("=" * 76)
print("② **境界の代数** — 積のとき")
print("=" * 76)
print("""  欲しい規則（§7.7.3.5）:
      (≤a)(≤b) → **≤ab**      |x|≤a, |y|≤b ⟹ |xy| ≤ ab
      (≥a)(≥b) → **≥ab**
      (≤a)(≥b) → **境界なし**  向きが打ち消し合う
      (=a)(≤b) → **≤ab**   ／  (=a)(≥b) → **≥ab**  ／  (=a)(=b) → **=ab**""")
def bound_mul_want(a, b):
    if a == 0 and b == 0: return 0            # 確定 × 確定 = 確定
    if a == -b and a != 0: return None        # **≤ × ≥ ⟹ 境界なし**
    if a == 0: return b
    if b == 0: return a
    return a                                  # 同じ向き（a == b）
print(f"\n  {'a':>8}{'b':>10}{'欲しい答え':>16}{'gate9':>10}{'gate27の同':>12}")
for a in (-1, 0, 1):
    for b in (-1, 0, 1):
        w = bound_mul_want(a, b)
        wn = "**境界なし**" if w is None else NAME_B[w]
        g9 = NAME_B[gate9(a, b)]
        g27 = NAME_B[gate27(a, b, 0)[0]] if abs(a+b) <= 1 else "（桁上げ）"
        print(f"  {NAME_B[a]:>8}{NAME_B[b]:>10}{wn:>16}{g9:>10}{g27:>12}")
print("""
  ⟹ **gate9 でも gate27 でもない。** 境界の積は別の表である
     （**≤ × ≥ が「境界なし」に落ちる**のが、既存のどちらにも無い）""")

print()
print("=" * 76)
print("③ **「境界なし」は 4 番目の状態が要るか** — 要らない")
print("=" * 76)
print("""  **境界なし** = 「|x| は [0, ∞) のどこか」。
  表現できる中で いちばん締まった主張は **「|x| ≥ 0」** —— **常に真。何も言っていない。**

  ⟹ **境界なし = （値 0, 境界トリット +1）**   ＝ **中身の無い下界**

  ⟹ **4 番目の状態は要らない。3 値で足りる。**
       境界トリット: **−1 = ≤v ／ 0 = =v ／ +1 = ≥v**
       そして **≥0 が「何も知らない」を意味する。**

  ⟹ **状態 = 2 トリット**（符号 3 × 境界 3 = 9 状態）。
     4ビット（16符号）より **小さく、しかもトリットのまま**。""")
