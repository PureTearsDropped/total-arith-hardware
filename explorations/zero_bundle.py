#!/usr/bin/env python3
"""**0 という関係束**（利用者）: `x−x=0` ／ `x×0=0` ／ `x/0=0` ／ `x^0=1`。

利用者: 「その 0 というのが **すべての x−x=0 と x×0=0 と x/0=0 と x^0=1 という関係である**」
        （[[relation-bundle-ontology]] の系）

**四つは並列か。** 導出して数える。並列でないなら、**手で足す必要があるのはどれか。**
"""
import numpy as np
from fractions import Fraction as Fr

print("="*92)
print("**① `x − x = 0` を出発点にする** — 0 を「変位の不在」として置く（＝加法の単位元）")
print("="*92)
print("""  定義:  **0 := x − x**（全ての x で同じ ⟹ 一意）
  帰結:  **x + 0 = x**（加法の単位元）""")

print()
print("="*92)
print("**② `x × 0 = 0` は導出される** — 分配律だけで")
print("="*92)
print("""    a·0 = a·(0 + 0)        ← 0 + 0 = 0（① より）
        = a·0 + a·0        ← **分配律**
    ⟹ 0 = a·0             ← 両辺から a·0 を引く（加法の逆元）

  **仮定は「0 は加法の単位元」＋「分配律」だけ。手で足していない。**""")
# 数値で確認（有理数・厳密）
ok = all(Fr(a)*Fr(0) == Fr(0) for a in (-7, -1, 0, 1, 3, 999))
print(f"  厳密な有理数で確認: **{ok}**")

print()
print("="*92)
print("**③ `x^0 = 1` も導出される** — exp が (+) → (×) の準同型であることから")
print("="*92)
print("""    x^a · x^b = x^{a+b}         ← **指数法則 ＝ exp が準同型**
    b = 0 と置く:
    x^a · x^0 = x^{a+0} = x^a
    ⟹ **x^0 = 1**（両辺を x^a で割る。x^a ≠ 0 が要る）

  あるいは: **準同型は単位元を単位元へ送る。** 0 は (+) の単位元、1 は (×) の単位元。
            exp: (ℝ, +) → (ℝ⁺, ×) ⟹ **exp(0) = 1** ⟹ x^0 = exp(0·ln x) = **1**

  **仮定は「0 は加法の単位元」＋「exp は準同型」だけ。手で足していない。**""")
ok = all(abs(float(np.power(float(x), 0.0)) - 1.0) < 1e-15 for x in (0.5, 1, 2, 7, 1e100))
print(f"  数値で確認: **{ok}**   ／  exp(0) = **{float(np.exp(0.0)):.1f}**（(×) の単位元）")

print()
print("="*92)
print("**④ `x / 0 = 0` だけが導出されない** — そこで群構造が壊れているから")
print("="*92)
print("""    x/0 を導くには **0 の乗法逆元 0⁻¹** が要る。だが:
        0 · z = 0   （② より、**全ての z で**）
    ⟹ **0 · z = 1 となる z は存在しない**（1 ≠ 0 なので）
    ⟹ **(ℝ, ×) は群でない。0 だけが逆元を持たない。**

  ⟹ **`x/0` は、群の外にある。導出できない。手で足すしかない。**
     そして足す値は自由 ⟹ **§7.6.7⑥⑧ の測定が効くのは、ここだけ。**""")

print("="*92); print("**数え上げ**"); print("="*92)
rows = [
    ("**x − x = 0**", "**定義**",  "0 を置く。ここが出発点"),
    ("x × 0 = 0",     "**定理**",  "① ＋ 分配律から。**自由度なし**"),
    ("x^0 = 1",       "**定理**",  "① ＋ exp が準同型から。**自由度なし**"),
    ("**x / 0 = 0**", "**選択**",  "**群の外。導出できない。値は自由** ⟹ 測定が要る"),
]
print(f"  {'関係':<18}{'種類':<12}{'理由'}")
for a,b,c in rows: print(f"  {a:<18}{b:<12}{c}")
print(f"""
  ⟹ **四つは並列ではない。① から ②③ が出て、④ だけが手で足すものである。**
     利用者の言う「関係束」は **一本の関係（x−x=0）が生成している**。
     **そして束が届かない一箇所（群が壊れる点）だけが、我々の選択だった。**

  ⟹ 本節の測定（§7.6.7⑥⑧）が意味を持つのは **④ だけ**。②③ を測るのは無意味である
     （定理なので、測る前から決まっている）。**監査人は三つを並べて測ろうとしていた。**""")
