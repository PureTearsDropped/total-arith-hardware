#!/usr/bin/env python3
"""**±MAX 付近の加減は、潰れうるか**（利用者）。§7.7.4.4 の「和は潰れない」を疑う。

  §7.7.4.4 は **厳密な値** で測った。Sterbenz は厳密な値の定理なので、和差は潰れない。
  **だが MAX が境界なら（|x| ≥ MAX）、真値は 1e400 と 1e400+1e−400 かもしれない。**
  その差は **−1e−400** ＝ **潰れの領域**。
"""
import warnings, numpy as np
from fractions import Fraction as Fr
warnings.filterwarnings("ignore")
MAX = np.finfo(np.float64).max
MIN = np.nextafter(0.0, 1.0)

print("=" * 88)
print("① **厳密な値なら**、和差は潰れない（§7.7.4.4。Sterbenz）")
print("=" * 88)
print(f"  {'式':<34}{'真値':>22}{'潰れたか':>12}")
for tag, a, b in [("1e308 − 1e308", Fr(10)**308, Fr(10)**308),
                  ("MAX − MAX",     Fr(MAX),     Fr(MAX)),
                  ("MAX − (MAX の1ulp下)", Fr(MAX), Fr(np.nextafter(MAX, 0)))]:
    d = a - b
    u = (d != 0) and abs(d) < Fr(MIN)
    ft = "**0（厳密）**" if d == 0 else f"~1e{len(str(d.numerator))-len(str(d.denominator)):+d}"
    print(f"  {tag:<34}{ft:>22}{('**潰れた**' if u else 'いいえ'):>12}")
print("\n  ⟹ **厳密な値では潰れない。** 0 になっても、それは **厳密な 0**（潰れではない）")

print()
print("=" * 88)
print("② **MAX が境界なら**（|x| ≥ MAX）、真値の差はどこへでも行く")
print("=" * 88)
print("  `(≥MAX) − (≥MAX)`: a ≥ MAX, b ≥ MAX。**真値の組を選ぶと:**")
print(f"\n  {'a の真値':>22}{'b の真値':>26}{'a − b':>22}{'どこ':>14}")
cases = [
    (Fr(10)**400, Fr(10)**400,               "**厳密に 0**"),
    (Fr(10)**400, Fr(10)**400 + Fr(10)**-400, "**潰れの領域**"),
    (Fr(10)**400, Fr(10)**400 + 1,            "普通の範囲"),
    (Fr(10)**400, Fr(10)**500,                "**溢れの領域**"),
]
for a, b, where in cases:
    d = a - b
    if d == 0: fd = "**0**"
    else:
        e = len(str(abs(d).numerator)) - len(str(abs(d).denominator))
        fd = f"~{'−' if d < 0 else ''}1e{e:+d}"
    fa = f"1e{len(str(a.numerator))-1}"
    fb = ("1e400+1e−400" if b == Fr(10)**400 + Fr(10)**-400 else
          "1e400+1" if b == Fr(10)**400 + 1 else f"1e{len(str(b.numerator))-1}")
    print(f"  {fa:>22}{fb:>26}{fd:>22}{where:>14}")
print("""
  ⟹ **利用者の通り。** `(≥MAX) − (≥MAX)` の真値は **潰れの領域にも溢れの領域にも行ける。**
  ⟹ **§7.7.4.4 の「和は潰れない」は、厳密な値についてだけ正しい。境界については偽。**""")

print()
print("=" * 88)
print("③ **設計は、それを既に捕まえているか**")
print("=" * 88)
print("""  §7.7.3.4 の規則: `(≥a) − (≥b)`（異符号／同じ端で飽和） → **境界なし**

  **境界なし** = 「|x| は [0, ∞) のどこか」 —— **潰れの領域も、溢れの領域も、その中に居る。**

  ⟹ **設計は正しい。捕まえている。** `≤` フラグを別に立てる必要は無い
     （**境界なし** が既に「どこへでも行きうる」と言っているので、`≤` を足しても締まらない）。

  ⟹ **だが 監査人の言い方が間違っていた:**
       ~~「潰れ → ≤ フラグ は 積と商だけ。和と差では起きない」~~
       **正しくは: 「厳密な値の和差は潰れない（Sterbenz）。**
                  **だが 境界どうしの和差は 境界なし へ落ち、そこは潰れの領域を含む」**

  ⟹ **表は閉じたままだが、閉じ方が違う:**
       和差が失うのは **符号**（§7.7.4.6）。**そして符号を失うとき、大きさも同時に失う**
       （`MAX − MAX` は §7.7.3.4 で「境界なし」＝ 大きさも不明）。
       **⟹ 和差は「一つだけ失う」のではなく、「符号を失うときは両方失う」。**""")
